using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Windows;
using System.Windows.Input;
using Fuzzware.Fuzzsaw.Common;
using Fuzzware.Schemas.AutoGenerated;

namespace Fuzzware.Fuzzsaw.Input.ViewModel
{
    /// <summary>
    /// The base class for all Input controls.  This should never be used directly.
    /// </summary>
    public class InputBaseViewModel : ViewModelBase
    {
        #region Dependency Properties

        static readonly DependencyProperty CommandProperty = DependencyProperty.Register("Command", typeof(ICommand), typeof(InputBaseViewModel));
        public ICommand Command
        {
            get { return (ICommand)GetValue(CommandProperty); }
            set { SetValue(CommandProperty, value); }
        }

        #endregion

        protected DataInputHandler m_oDataInputHandler;

        public InputBaseViewModel()
        {
            m_oDataInputHandler = new DataInputHandler();
        }

        public ICommand SelectCommand
        {
            get { return Command; }
        }
    }
}
