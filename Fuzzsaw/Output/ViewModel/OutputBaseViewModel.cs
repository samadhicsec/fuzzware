using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Windows;
using System.Windows.Input;
using Fuzzware.Fuzzsaw.Common;
using Fuzzware.Schemas.AutoGenerated;

namespace Fuzzware.Fuzzsaw.Output
{
    /// <summary>
    /// The base class for all Output controls.  This should never be used directly.
    /// </summary>
    public class OutputBaseViewModel : ViewModelBase
    {
        #region Dependency Properties

        static readonly DependencyProperty CommandProperty = DependencyProperty.Register("Command", typeof(ICommand), typeof(OutputBaseViewModel));
        public ICommand Command
        {
            get { return (ICommand)GetValue(CommandProperty); }
            set { SetValue(CommandProperty, value); }
        }

        #endregion

        protected EvaluationMethod m_oEvaluationMethod;

        public OutputBaseViewModel()
        {
            m_oEvaluationMethod = new EvaluationMethod();
        }

        public ICommand SelectCommand
        {
            get { return Command; }
        }
    }
}
