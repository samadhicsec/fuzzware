        static readonly DependencyProperty FileStoreProperty = DependencyProperty.Register("FileStore", typeof(FileStore), typeof(FileStoreControl));
        public FileStore FileStore
        {
            get { return (FileStore)GetValue(FileStoreProperty); }
            set { SetValue(FileStoreProperty, value); }
        }

        static readonly DependencyProperty DescriptionProperty = DependencyProperty.Register("Description", typeof(String), typeof(FileStoreControl));
        public String Description
        {
            get { return (String)GetValue(DescriptionProperty); }
            set { SetValue(DescriptionProperty, value); }
        }

        #endregion

        public FileStoreControl()
        {
            InitializeComponent();

            FileStore = new FileStore();
            FileStore.Directory = Fuzzsaw.DefaultOutputDir;
            FileStore.FileExtension = Fuzzsaw.DefaultOutputExt;

            Description = "Choose an output directory and file extension";
        }
