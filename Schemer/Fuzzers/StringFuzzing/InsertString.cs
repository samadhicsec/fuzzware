using System;
using System.Collections.Generic;
using System.Text;
using System.Reflection;
using Fuzzware.Common;
using Fuzzware.Common.XML;
using Fuzzware.Schemer.AutoGenerated;

namespace Fuzzware.Schemer.Fuzzers.StringFuzzing
{
    class InsertString : StringValueFuzzerBase
    {
        public InsertString(ConfigData oConfigData, PreCompData oPreCompData, ObjectDBEntry oSchemaElement)
            : base(oConfigData, oPreCompData, oSchemaElement)
        {

        }

        protected override void AssignValueFuzzers(StringValueFuzzersType FuzzersType)
        {
            if (FuzzersType.InsertString != null)
            {
                Add("InsertString", InsertStringFn);
                Strings = GetValueGroup(FuzzersType.InsertString.ValueGroupRef);
                oValueRange = GetValueRange(FuzzersType.InsertString.ValueRangeRef, ValueConfig.StringRange);
            }
        }

        /// <summary>
        /// Insert into the current string value a value from the user configured list.
        /// </summary>
        public bool InsertStringFn(int FuzzIndex, int NodeIndex)
        {
            if (null == Strings)
                return false;

            return InsertStringValue(FuzzIndex, NodeIndex, Strings, oValueRange);
        }
    }
}
