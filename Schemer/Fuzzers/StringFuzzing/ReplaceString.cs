using System;
using System.Collections.Generic;
using System.Text;
using System.Reflection;
using Fuzzware.Common;
using Fuzzware.Common.XML;
using Fuzzware.Schemer.AutoGenerated;

namespace Fuzzware.Schemer.Fuzzers.StringFuzzing
{
    class ReplaceString : StringValueFuzzerBase
    {
        public ReplaceString(ConfigData oConfigData, PreCompData oPreCompData, ObjectDBEntry oSchemaElement)
            : base(oConfigData, oPreCompData, oSchemaElement)
        {

        }

        protected override void AssignValueFuzzers(StringValueFuzzersType FuzzersType)
        {
            if (FuzzersType.ReplaceString != null)
            {
                Add("ReplaceString", ReplaceStringFn);
                Strings = GetValueGroup(FuzzersType.ReplaceString.ValueGroupRef);
            }
        }

        /// <summary>
        /// Replaces the current string value with a value from the user configured list.
        /// </summary>
        public bool ReplaceStringFn(int FuzzIndex, int NodeIndex)
        {
            if (null == Strings)
                return false;

            return ReplaceStringValue(FuzzIndex, NodeIndex, Strings);
        }
    }
}
