using System;
using System.Collections.Generic;
using System.Text;
using Fuzzware.Convert2XML;
using Fuzzware.Schemas.AutoGenerated;

namespace Fuzzware.Schemer.PreOutputHandlers
{
    class PreOutputHandlersFactory
    {
        public static PreOutputHandler GetPreOutputHandler(PreOutput oPreOutput, InputHandler oInputHandler)
        {
            if (oInputHandler is ProtocolDefnInputHandler)
                return new ProtocolPreOutputHandler();
            else if ((null != oPreOutput) && (null != oPreOutput.UserDefinedPreOutput))
            {
                if (oPreOutput.UserDefinedPreOutput is UserDefinedCode)
                    return new ExtensiblePreOutputHandler();
            }

            return null;
        }
    }
}
